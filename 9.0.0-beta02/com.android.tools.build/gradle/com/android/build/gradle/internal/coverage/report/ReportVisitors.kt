package com.android.build.gradle.internal.coverage.report

import com.android.Version
import org.jacoco.report.FileMultiReportOutput
import org.jacoco.report.IReportVisitor
import org.jacoco.report.html.HTMLFormatter
import org.jacoco.report.xml.XMLFormatter
import java.io.IOException
import java.io.OutputStream
import java.util.Locale

enum class ReportType {
    HTML,
    XML
}

@Throws(IOException::class)
internal fun createHtmlReportVisitor(output: FileMultiReportOutput): IReportVisitor {
    val htmlFormatter = HTMLFormatter()
    htmlFormatter.outputEncoding = "UTF-8"
    htmlFormatter.locale = Locale.US
    htmlFormatter.footerText = (
            "Generated by the Android Gradle plugin "
                    + Version.ANDROID_GRADLE_PLUGIN_VERSION)
    return htmlFormatter.createVisitor(output)
}

@Throws(IOException::class)
internal fun createXmlReportVisitor(reportOutput: OutputStream): IReportVisitor {
    val xmlFormatter = XMLFormatter()
    xmlFormatter.setOutputEncoding("UTF-8")
    return xmlFormatter.createVisitor(reportOutput)
}
